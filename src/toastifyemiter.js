import { toast } from "react-toastify";
import 'react-toastify/dist/ReactToastify.css';

export function toastEmiter( message ) {
    toast(message, {
        position: "top-right",
        autoClose: 2000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: false,
        progress: undefined,
        theme: "light",
    })
}

